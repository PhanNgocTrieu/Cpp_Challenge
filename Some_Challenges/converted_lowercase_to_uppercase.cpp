#include <iostream>
#include <cstring>
//#include <locale>

int main() {

	char enter_String[50];
	std::cin.getline(enter_String,50);
	char upperCase;
	int32_t i = 0;


	// converted lowercase to uppercase
	while (enter_String[i])
	{
		upperCase = enter_String[i];
		putchar(toupper(upperCase));
		i++;
	}

	return 0;
}
